int perimited(std::vector<std::vector<char>> map, int x, int y, std::vector<std::vector<bool>>& visited, char c){
    visited[x][y] = true;
    int result = 0;
    if (x + 1 >= map.size() || map[x+1][y] != c) result++;
    if (y + 1 >= map[0].size() || map[x][y+1] != c) result++;
    if (x - 1 < 0 || map[x-1][y] != c) result++;
    if (y - 1 < 0 || map[x][y-1] != c) result++;

    if (x + 1 < map.size() && !visited[x+1][y] && map[x+1][y] == c) {
        result += perimited(map, x+1, y, visited, c);
    }
    if (y + 1 < map[0].size() && !visited[x][y+1] && map[x][y+1] == c){
        result += perimited(map, x, y+1, visited, c);
    }
    if (x - 1 >= 0 && !visited[x-1][y] && map[x-1][y] == c){
        result += perimited(map, x-1, y, visited, c);
    }
    if (y - 1 >= 0 && !visited[x][y-1] && map[x][y-1] == c){
        result +=  perimited(map, x, y-1, visited, c);
    }

    return result;
}

int area(std::vector<std::vector<char>> map, int x, int y, std::vector<std::vector<bool>>& visited, char c){
    visited[x][y] = true;
    int result = 0;
    if (x + 1 < map.size() && !visited[x+1][y] && map[x+1][y] == c) {
        result += area(map, x+1, y, visited, c);
    }
    if (y + 1 < map[0].size() && !visited[x][y+1] && map[x][y+1] == c){
        result += area(map, x, y+1, visited, c);
    }
    if (x - 1 >= 0 && !visited[x-1][y] && map[x-1][y] == c){
        result += area(map, x-1, y, visited, c);
    }
    if (y - 1 >= 0 && !visited[x][y-1] && map[x][y-1] == c){
        result +=  area(map, x, y-1, visited, c);
    }
    return result + 1;
}

