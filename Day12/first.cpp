    std::vector<std::vector<bool>> visited2 = visited;

    int area1 = area(map, x, y, visited2, c);
    int perimiter2 = perimited(map, x, y, visited, c);
    std::cout << "area = " << area1 << " perimiter = " << perimiter2 << std::endl; 
